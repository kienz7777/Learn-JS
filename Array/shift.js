// shift() will remove the first element of an array and 
//return that element. After execution, this function will
//change the size of the affected array.


var myFish = ['angel', 'clown', 'mandarin', 'surgeon'];

var shifted = myFish.shift();

console.log(myFish);      // [ 'clown', 'mandarin', 'surgeon' ]
console.log(shifted);    // angle
 
